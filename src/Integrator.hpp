#pragma once
#include <functional>
#include <mutex>

/**
 * @brief Выполняет численное интегрирование заданной функции.
 *
 * Реализует метод трапеций: отрезок [a, b] разбивается на равные
 * подотрезки шириной h, площадь каждой трапеции суммируется.
 */
class Integrator {
public:
    /**
     * @brief Вычисляет определённый интеграл функции f на отрезке [a, b] методом трапеций.
     *
     * Формула: I ≈ h * (f(a)/2 + f(x1) + f(x2) + ... + f(b)/2)
     *
     * @param f Подынтегральная функция вида double(double).
     * @param a Левый предел интегрирования.
     * @param b Правый предел интегрирования.
     * @param h Шаг интегрирования (ширина одной трапеции).
     * @return  Приближённое значение интеграла.
     */
    double integrate(const std::function<double(double)>& f, double a, double b, double h);
};
